#!/bin/bash

try() {
  expected="$@"
  input=`cat -`

  actual=`echo $input | ./main`

  if [ "$actual" = "$expected" ]; then
    echo "$input => $actual"
  else
    echo "==="
    echo "$input => $expected expected, but got $actual"
    exit 1
  fi
}

## test case 1
cat << EOF | try ABC
999
EOF

## test case 2
cat << EOF | try ABD
1000
EOF

## test case 2
cat << EOF | try ABD
1481
EOF
